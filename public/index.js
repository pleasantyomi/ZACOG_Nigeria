function hamburger() {
    let x=
    document.getElementById("hamburger-contents");
    if (x.style.display === "none") {
        x.style.display= "block";
    } else {
        x.style.display= "none";
    }
}   

function hamburgerClose() {
    let x=
    document.getElementById("hamburger-contents");
    if (x.style.display === "none") {
        x.style.display= "block";
    } else {
        x.style.display= "none";
    }
}   